package demo.example.plugins

import demo.example.services.ApplicationService
import io.ktor.http.HttpStatusCode
import io.ktor.server.application.Application
import io.ktor.server.application.call
import io.ktor.server.application.install
import io.ktor.server.plugins.statuspages.StatusPages
import io.ktor.server.plugins.swagger.swaggerUI
import io.ktor.server.response.respondText
import io.ktor.server.routing.get
import io.ktor.server.routing.route
import io.ktor.server.routing.routing
import org.koin.ktor.ext.inject


fun Application.configureRouting() {
    val appService by inject<ApplicationService>()

    install(StatusPages) {
        exception<Throwable> { call, cause ->
            call.respondText(text = "500: $cause", status = HttpStatusCode.InternalServerError)
        }
    }

    routing {
        swaggerUI(path = "/swagger", swaggerFile = "swagger.yaml")
        route("/"){
            get { call.respondText(appService.respondHello()) }
        }
    }
}
